    private void jBpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBpesquisarActionPerformed
        // TODO add your handling code here:
        try{
            Aluno a = new Aluno();
            ManterAlunos dao = new ManterAlunos();
            a.setCodigo(Integer.valueOf(jTcodigo.getText()));
            dao.PesquisarRegistro(a);
            jTnome.setText(a.getNome());
            jTemail.setText(a.getEmail());
            jTcodigo.setEditable(false);//evita que o usuario troque o codigo
        }catch(Exception e){
            System.out.println("Erro " + e.getMessage());
        }
    }//GEN-LAST:event_jBpesquisarActionPerformed

    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        try{
        Aluno a = new Aluno();
        ManterAlunos dao = new ManterAlunos();
        a.setCodigo(Integer.valueOf(jTcodigo.getText()));
        a.setNome(jTnome.getText());
        a.setEmail(jTemail.getText());
        dao.editarAluno(a);
        jTnome.setText("");
        jTemail.setText("");
        jTcodigo.setText("");
        jTcodigo.requestFocus();
        }catch(Exception e){
            System.out.println("Erro " + e.getMessage());
        }
    }//GEN-LAST:event_jBalterarActionPerformed

